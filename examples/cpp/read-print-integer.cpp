// C++ Program to Print Number Entered by User

// In this example, you'll learn to print the number entered by a user using C++ cout statement.

#include <iostream>
using namespace std;

int main()
{    
    int number;

    cout << "Enter an integer: ";
    cin >> number;

    cout << "You entered " << number;    
    return 0;
}

