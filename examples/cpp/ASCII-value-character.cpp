// C++ Program to Find ASCII Value of a Character

// In this example, you will learn to find ASCII value of a character in C++.

#include <iostream>
using namespace std;

int main()
{
 char c;
 cout << "Enter a character: ";
 cin >> c;
 cout << "ASCII Value of " << c << " is " << int(c);
 return 0;
}

