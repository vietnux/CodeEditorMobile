// C Program to Display Characters from A to Z Using Loop

// Source code to display character from A to Z using loops in C programming....

#include <stdio.h>
int main()
{
    char c;

    for(c = 'A'; c <= 'Z'; ++c)
       printf("%c ", c);
    
    return 0;
}

